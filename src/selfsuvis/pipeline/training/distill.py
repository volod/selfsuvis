"""Knowledge distillation pipeline: two-stage compression chain.

**Stage 1 (Gemma/DINOv3 → ViT-S/14)**

The teacher is the SSL fine-tuned DINOv3 ViT-B/14 backbone (or a GemmaVisionTeacher
wrapping the Gemma 4 vision encoder for maximum-hydration runs).  The student is
DINOv2 ViT-S/14 (~22M params, 384-dim) vs the teacher's ViT-B/14 (~86M, 768-dim).
After Stage 1 the student is ~4× smaller and ~2× faster.  Requires ≥24 GB VRAM when
using the Gemma teacher; ~8 GB with the DINOv3 teacher.

Training uses Relational Knowledge Distillation with Distance + Angle losses (RKD-DA)
plus a KoLeo spread regulariser and an optional cosine anchor loss:

    L = λ_D · L_RKD_dist  +  λ_A · L_RKD_angle  +  λ_kd · L_cosine  +  λ_koleo · L_KoLeo

RKD-DA preserves pairwise neighbourhood topology in the student embedding space,
which directly optimises retrieval Recall@K.  Defaults: λ_D=25, λ_A=50 (paper values).

A temporary projection head (Linear s_dim→t_dim, orthogonal init) aligns the student
output space with the teacher's during training; it is discarded after training.

**Stage 2 (ViT-S/14 → EfficientViT-B1)**

Optional second compression hop for edge deployment.  The Stage 1 ViT-S/14 student
becomes the Stage 2 teacher; EfficientViT-B1 (~9M params, 384-dim) is the student.
Both teacher and student are 384-dim so RKD-A provides no benefit — Stage 2 uses
RKD-D + KoLeo only (λ_A=0).  Requires only ~2 GB VRAM.

Use :func:`run_distillation_efficientvit` for Stage 2.  The ``stage`` field in
:class:`DistillConfig` records which stage produced a checkpoint (1 or 2).
"""

